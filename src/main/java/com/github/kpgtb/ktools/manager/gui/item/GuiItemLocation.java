package com.github.kpgtb.ktools.manager.gui.item;

import java.util.Objects;

/**
 * Object that represents location of item in container
 */
public class GuiItemLocation {
    private final int x;
    private final int y;

    /**
     * Constructor of location
     * @param x X position in container
     * @param y Y position in container
     */
    public GuiItemLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public int hashCode() {
        return Objects.hash(x, y);
    }

    @Override
    public boolean equals(Object obj) {
        if(this == obj) {
            return true;
        }

        if(obj == null || obj.getClass() != this.getClass()) {
            return false;
        }

        GuiItemLocation iLoc = (GuiItemLocation) obj;
        return this.x == iLoc.x && this.y == iLoc.y;
    }
}
